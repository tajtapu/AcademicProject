
public class RelationalOperator {

	public static void main(String[] args) {
		int num1=50, num2=100;
		boolean result;
		result=num1<num2;
		System.out.println(result);
		System.out.println(num1<num2);
		System.out.println(num1>num2);
		System.out.println(num1<=num2);
		System.out.println(num1>=num2);
		System.out.println(num1==num2);
		System.out.println(num1!=num2);
		

	}

}
